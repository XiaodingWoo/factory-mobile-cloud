def _query_value(name: str, default: str = "") -> str:
    try:
        value = st.query_params.get(name, default)
    except Exception:
        return default
    if isinstance(value, list):
        return str(value[0] if value else default)
    return str(value or default)


def current_lang(default: str = "en") -> str:
    query_lang = _query_value("lang", "").strip()
    if query_lang in TRANSLATIONS:
        st.session_state["language"] = query_lang
        return query_lang
    session_lang = str(st.session_state.get("language", "")).strip()
    if session_lang in TRANSLATIONS:
        return session_lang
    st.session_state["language"] = default
    return default


def language() -> str:
    return current_lang()


def t(key: str, **values: object) -> str:
    text = TRANSLATIONS.get(language(), TRANSLATIONS["en"]).get(key)
    if text is None:
        text = TRANSLATIONS["en"].get(key, key)
    return text.format(**values)


def language_selector(key: str = "language_selector") -> str:
    current = language()
    labels = {"English": "en", "简体中文": "zh-CN"}
    selected = st.radio(
        t("language.choose"),
        list(labels),
        index=0 if current == "en" else 1,
        horizontal=True,
        key=key,
    )
    chosen = labels[selected]
    st.session_state["language"] = chosen
    try:
        st.query_params["lang"] = chosen
    except Exception:
        pass
    return chosen
